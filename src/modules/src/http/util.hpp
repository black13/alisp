#pragma once

#include <stdio.h>
#include <time.h>
#include <string>

namespace http
{

namespace detail
{

inline std::string gat_date()
{
    char buf[1024];
    time_t now   = time(0);
    struct tm tm = *gmtime(&now);
    strftime(buf, sizeof(buf), "%a, %d %b %Y %H:%M:%S %Z", &tm);
    return { buf };
}

}

}
